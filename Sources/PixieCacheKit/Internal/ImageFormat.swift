import Foundation

@available(iOS 15.0, *)
/// Enumeration representing supported image formats in PixieCacheKit.
public enum ImageFormat: String {
    /// `jpeg` stores images in .jpeg format.
    case jpeg = ".jpeg"
    
    /// `png` stores images in .png format.
    case png = ".png"
}
